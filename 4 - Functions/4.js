// Functions

// Substraction

let resta = function(x, i) {
  return x - i;
};
console.log("substraction result " + resta(69, 15));

// Division

let division = function(x, i) {
  return x / i;
};
console.log("division result " + division(56, 7));

// Multiplication

let multi = function(x, i) {
  return x * i;
};
console.log("multiplication result " + multi(44, 23));

// Percentage

let percent = function(x, i) {
  return x * i / 100;
};
console.log("percentage result " + percent(20, 345));

// Speed

let speed = function(x, i) {
  return x / i;
};
console.log(speed(2000, 8) + "km/h");
